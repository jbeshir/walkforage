export const FOODS_BY_ID = createByIdMap(FOODS);

// Helper functions
export function getFoodsByBiome(biome: BiomeCode | string): FoodType[] {
  return FOODS.filter((f) => f.biomes.includes(biome as BiomeCode));
}

export function getFoodsByRealmBiome(realmBiome: string): FoodType[] {
  return FOODS.filter((f) => f.realmBiomes?.includes(realmBiome));
}

export function getFoodsByRealm(realm: string): FoodType[] {
  return FOODS.filter((f) => f.nativeRealms?.includes(realm));
}

export function getFoodsByCategory(category: string): FoodType[] {
  return FOODS.filter((f) => f.category === category);
}

// Get all biomes that have forageable foods
export function getFoodBiomes(): BiomeCode[] {
  const biomes = new Set<BiomeCode>();
  FOODS.forEach((f) => f.biomes.forEach((b) => biomes.add(b)));
  return Array.from(biomes);
}

// Get all realm-biome codes that have food mappings
export function getMappedFoodRealmBiomes(): string[] {
  const realmBiomes = new Set<string>();
  FOODS.forEach((f) => f.realmBiomes?.forEach((rb) => realmBiomes.add(rb)));
  return Array.from(realmBiomes);
}
